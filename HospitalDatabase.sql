CREATE DATABASE HOSPITAL_MANAGEMENT;

USE HOSPITAL_MANAGEMENT;


CREATE TABLE location (
  city VARCHAR(50),
  country VARCHAR(50),
  area_code VARCHAR(10) PRIMARY KEY
);


CREATE TABLE hospital (
  facility_number INT PRIMARY KEY,
  system_number VARCHAR(50),
  hospital_name VARCHAR(50),
  location_id VARCHAR(10),
  FOREIGN KEY (location_id) REFERENCES location(area_code) ON DELETE CASCADE
);

CREATE TABLE EMPLOYEE(
    start_date DATE,
    end_date DATE,
    Productive_hours INT,
    emp_name VARCHAR(50),
    emp_id INT UNIQUE PRIMARY KEY,
    hospital_id INT NOT NULL,
    FOREIGN KEY (hospital_id) REFERENCES hospital(facility_number) ON DELETE CASCADE
);

CREATE TABLE DOCTOR(
    doctor_id INT UNIQUE,
    speciality VARCHAR(50),
    employee_id INT,
    INDEX doctor_id_index (doctor_id),
    PRIMARY KEY (employee_id,doctor_id),
    FOREIGN KEY (employee_id) REFERENCES EMPLOYEE(emp_id) ON DELETE CASCADE
);

CREATE TABLE NURSE(
    nurse_id INT,
    assigned_room INT,
    employee_id INT,
    PRIMARY KEY (nurse_id,employee_id),
    FOREIGN KEY (employee_id) REFERENCES EMPLOYEE(emp_id) ON DELETE CASCADE
);

CREATE TABLE MEDICAL_REPORT(
    assigned_date DATE,
    discharge_date DATE,
    treatment_record INT,
    report_id INT PRIMARY KEY
);

CREATE TABLE RECEPTIONIST(
    desk_number INT,
    employee_id INT,
    PRIMARY KEY (employee_id), 
    FOREIGN KEY (employee_id) REFERENCES EMPLOYEE(emp_id) ON DELETE CASCADE
);

CREATE TABLE Benefits (
  benefit_id INT AUTO_INCREMENT,
  employee_id INT,
  PRIMARY KEY (benefit_id),
  FOREIGN KEY (employee_id) REFERENCES EMPLOYEE(emp_id) ON DELETE CASCADE
);

CREATE TABLE HALF_BENEFITS(
    benefit_id INT PRIMARY KEY,
    half_health_coverage VARCHAR(50),
    FOREIGN KEY (benefit_id) REFERENCES Benefits(benefit_id) ON DELETE CASCADE
);

CREATE TABLE NO_BENEFITS(
    benefit_id INT PRIMARY KEY,
    no_health_coverage VARCHAR(50),
    FOREIGN KEY (benefit_id) REFERENCES Benefits(benefit_id) ON DELETE CASCADE
);

CREATE TABLE FULL_BENEFITS(
    benefit_id INT PRIMARY KEY,
    half_health_coverage VARCHAR(50),
    FOREIGN KEY (benefit_id) REFERENCES Benefits(benefit_id) ON DELETE CASCADE
);

CREATE TABLE BILL(
    reciept_number INT PRIMARY KEY,
    employee_id INT,
    amount INT,
    report_id INT,
    FOREIGN KEY(report_id) REFERENCES MEDICAL_REPORT(report_id) ON DELETE CASCADE,
    FOREIGN KEY(employee_id) REFERENCES RECEPTIONIST(employee_id) ON DELETE CASCADE
);

CREATE TABLE CARD_PAYMENT(
    name_on_card VARCHAR(50),
    card_number INT,
    exp_date DATE,
    cvv INT,
    reciept_num INT,
    FOREIGN KEY(reciept_num) REFERENCES BILL(reciept_number) ON DELETE CASCADE
);

CREATE TABLE CASH_PAYMENT(
    reciept_num INT PRIMARY KEY,
    FOREIGN KEY(reciept_num) REFERENCES BILL(reciept_number) ON DELETE CASCADE
);

CREATE TABLE CABIN(
    cabin_type VARCHAR(50),
    cabin_number INT PRIMARY KEY
);






CREATE TABLE PATIENT(
    report_id INT UNIQUE,
    phone INT,
    address VARCHAR(50),
    patient_name VARCHAR(50),
    health_care_num INT PRIMARY KEY UNIQUE,
    facility_num INT,
    cabin_number INT,
    doctor_id INT,
    nurse_id INT,
     FOREIGN KEY(report_id) REFERENCES MEDICAL_REPORT(report_id)   ON DELETE CASCADE,
     FOREIGN KEY(facility_num) REFERENCES HOSPITAL(facility_number) ON DELETE CASCADE,
     FOREIGN KEY(cabin_number) REFERENCES CABIN(cabin_number) ON DELETE CASCADE,
     FOREIGN KEY(doctor_id) REFERENCES DOCTOR(doctor_id) ON DELETE CASCADE,
     FOREIGN KEY(nurse_id) REFERENCES NURSE(nurse_id) ON DELETE CASCADE
);


CREATE TABLE ADMIN(
    id INT AUTO_INCREMENT PRIMARY key, 
    username VARCHAR(50) NOT NULL,
    password VARCHAR(50) NOT NULL
);

